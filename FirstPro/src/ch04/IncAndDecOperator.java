package ch04;

public class IncAndDecOperator {
	public static void main(String[] args) {
		int a = 10;
		int b = 20;

		System.out.println("a = " + a);
		System.out.println("++a = " + ++a);
		System.out.println("a++ = " + a++);
		System.out.println("a = " + a);
		System.out.println("a-- = " + a--);
		System.out.println("--a = " + --a);
		System.out.println("a = " + a);
//		System.out.println("b = "+ b);

	}
}
